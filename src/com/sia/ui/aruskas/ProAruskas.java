    private void mat(){
        String isiTtlSld = String.valueOf(isiTotalSaldo.getValue());
        BigDecimal ttlsld = new BigDecimal(isiTtlSld);
        String isiTtlPeng = String.valueOf(isiTotalPengeluaran.getValue());
        BigDecimal ttlpeng = new BigDecimal(isiTtlPeng);
        String isiTtlPend = String.valueOf(isiTotalPendapatan1.getValue());
        BigDecimal ttlpend = new BigDecimal(isiTtlPend);
        
        BigDecimal yyy = ttlsld.add(ttlpend);
        isiTotalPemasukan.setValue(yyy);
        
        if( ttlpeng.compareTo(ttlsld) < 0){//ttlsld>ttlpeng
                BigDecimal zzz = ttlsld.subtract(ttlpeng);
                isiSaldoEsok.setValue(zzz);
                isiSisaPemasukan.setText(isiTtlPend);
        }else{
                //BigDecimal yyy = ttlsld.add(ttlpend);
                BigDecimal zzz = yyy.subtract(ttlpeng);
                isiSisaPemasukan.setValue(zzz);
        }
        
    }
    
