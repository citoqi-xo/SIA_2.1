    public void saveNlC(){
        if(isiCash.getText().isEmpty()){
            if(isiBankA.getText().isEmpty()){
                if(isiBankB.getText().isEmpty()){
                    if(isiBankC.getText().isEmpty()){
                        isiCash.setText("0");
                        isiBankA.setText("0");
                        isiBankB.setText("0");
                        isiBankC.setText("0");
                    }else{
                        isiCash.setText("0");
                        isiBankA.setText("0");
                        isiBankB.setText("0");
                    }
                }else{
                    isiCash.setText("0");
                    isiBankA.setText("0");
                }
            }else{
                isiCash.setText("0");
            }
        }else{
            if(isiBankA.getText().isEmpty()){
                if(isiBankB.getText().isEmpty()){
                    if(isiBankC.getText().isEmpty()){
                        isiBankA.setText("0");
                        isiBankB.setText("0");
                        isiBankC.setText("0");
                    }else{
                        isiBankA.setText("0");
                        isiBankB.setText("0");
                    }
                }else{
                    isiBankA.setText("0");
                    isiBankC.setText("0");
                }
            }else{
                if(isiBankB.getText().isEmpty()){
                    if(isiBankC.getText().isEmpty()){
                        isiBankB.setText("0");
                        isiBankC.setText("0");
                    }else{
                        isiBankB.setText("0");
                    }
                }else{
                    if(isiBankC.getText().isEmpty()){
                        isiBankC.setText("0");
                    }
                }
            }
        }
    }

